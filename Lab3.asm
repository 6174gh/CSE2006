;Add two 1D matrices 

ASSUME CS:CODE,DS:DATA
DATA SEGMENT
    MAT1 DW 1010H, 1020H, 1030H, 1040H, 1050H
    MAT2 DW 2010H, 2020H, 2030H, 2040H, 2050H
    MAT3 DW 05H DUP(?)
DATA ENDS
CODE SEGMENT
    START:
        MOV AX, DATA
        MOV DS, AX
        MOV CL, 05H
        MOV AX, 00H
        ADD AX, MAT1[SI]
        ADD AX, MAT2[SI]
        MOV MAT3[SI], AX
        HLT
CODE ENDS
    END START